    def _get_volatility_from_akshare(self, code: str, days: int = 20) -> float:
        """从AkShare获取历史数据计算波动率"""
        try:
            if not (self.manager.ak and self.manager.ak._ak):
                return None
            
            import pandas as pd
            from datetime import datetime, timedelta
            
            end_date = datetime.now()
            start_date = end_date - timedelta(days=days+10)
            
            df = self.manager.ak._ak.stock_zh_a_hist(
                symbol=code,
                period='daily',
                start_date=start_date.strftime('%Y%m%d'),
                end_date=end_date.strftime('%Y%m%d'),
                adjust='qfq'
            )
            
            if df is not None and not df.empty and len(df) >= 5:
                df['close'] = pd.to_numeric(df['close'], errors='coerce')
                df = df.dropna(subset=['close'])
                df['return'] = df['close'].pct_change()
                
                daily_returns = df['return'].dropna()
                if len(daily_returns) >= 5:
                    daily_std = daily_returns.std()
                    annual_volatility = daily_std * (252 ** 0.5) * 100
                    return round(annual_volatility, 2)
        except Exception:
            pass
        return None
    
    def _get_volatility_from_baostock(self, code: str, days: int = 20) -> float:
        """从BaoStock获取历史数据计算波动率"""
        try:
            if not self.manager.baostock:
                return None
            
            df = self.manager.baostock.get_daily_data(code, days=days+5)
            if df is not None and not df.empty and len(df) >= 5:
                df['close'] = pd.to_numeric(df['close'], errors='coerce')
                df = df.dropna(subset=['close'])
                df['return'] = df['close'].pct_change()
                
                daily_returns = df['return'].dropna()
                if len(daily_returns) >= 5:
                    daily_std = daily_returns.std()
                    annual_volatility = daily_std * (252 ** 0.5) * 100
                    return round(annual_volatility, 2)
        except Exception:
            pass
        return None
    
    def _get_volatility_from_realtime(self, code: str) -> float:
        """使用实时数据估算波动率（简化版）"""
        try:
            # 使用当天的振幅估算
            df = self.manager.get_realtime_quotes([code])
            if df is None or df.empty:
                return None
            
            row = df.iloc[0]
            high = float(row.get('high', 0))
            low = float(row.get('low', 0))
            price = float(row.get('price', 0))
            
            if high > 0 and low > 0 and price > 0:
                # 计算当日振幅
                amplitude = (high - low) / price * 100
                # 估算年化波动率（简化公式）
                # 假设日振幅与波动率有相关性
                estimated_vol = amplitude * 8  # 经验系数
                return round(min(estimated_vol, 100), 2)  # 上限100%
        except Exception:
            pass
        return None
    
    def _enrich_with_fundamentals(self, df: pd.DataFrame) -> pd.DataFrame:
        """为DataFrame添加ROE、波动率、技术指标和Alpha因子数据"""
        if df.empty:
            return df
        
        # 添加ROE列
        df['roe'] = None
        # 添加波动率列
        df['volatility_20d'] = None
        # 添加技术指标列
        df['rsi_14'] = None
        df['macd_histogram'] = None
        # 添加Alpha因子列
        df['alpha001'] = None
        df['alpha014'] = None
        df['alpha028'] = None
        # 添加价格位置列
        df['price_position'] = None
        
        print("📊 正在获取基本面数据、技术指标和Alpha因子...")
        
        for idx, row in df.iterrows():
            code = row.get('code', '')
            if not code:
                continue
            
            try:
                # 获取ROE
                roe = self._get_stock_roe(code)
                if roe is not None:
                    df.at[idx, 'roe'] = roe
                
                # 获取波动率
                vol = self._get_stock_volatility(code)
                if vol is not None:
                    df.at[idx, 'volatility_20d'] = vol
                
                # 获取历史K线数据（用于计算技术指标）
                hist_df = self._get_history_k_data_for_code(code, days=30)
                if hist_df is not None and not hist_df.empty and len(hist_df) >= 20:
                    try:
                        # 确保列名正确
                        hist_df['close'] = pd.to_numeric(hist_df['close'], errors='coerce')
                        hist_df['open'] = pd.to_numeric(hist_df['open'], errors='coerce')
                        hist_df['high'] = pd.to_numeric(hist_df['high'], errors='coerce')
                        hist_df['low'] = pd.to_numeric(hist_df['low'], errors='coerce')
                        hist_df['volume'] = pd.to_numeric(hist_df['volume'], errors='coerce')
                        
                        # 计算RSI
                        rsi = self._calculate_rsi(hist_df['close'], period=14)
                        if rsi is not None:
                            df.at[idx, 'rsi_14'] = rsi
                        
                        # 计算MACD
                        macd_dict = self._calculate_macd(hist_df['close'])
                        if macd_dict['histogram'] is not None:
                            df.at[idx, 'macd_histogram'] = macd_dict['histogram']
                        
                        # 计算价格位置（20日）
                        high_20 = hist_df['high'].tail(20).max()
                        low_20 = hist_df['low'].tail(20).min()
                        close_current = hist_df['close'].iloc[-1]
                        if high_20 > low_20:
                            price_pos = (close_current - low_20) / (high_20 - low_20) * 100
                            df.at[idx, 'price_position'] = price_pos
                        
                        # 计算Alpha因子
                        alpha001 = self._calculate_alpha001(hist_df)
                        df.at[idx, 'alpha001'] = alpha001
                        
                        alpha014 = self._calculate_alpha014(hist_df)
                        df.at[idx, 'alpha014'] = alpha014
                        
                        alpha028 = self._calculate_alpha028(hist_df)
                        df.at[idx, 'alpha028'] = alpha028
                        
                    except Exception as e:
                        print(f"⚠️ 计算{code}技术指标失败: {e}")
                    
            except Exception as e:
                print(f"⚠️ 获取{code}基本面数据失败: {e}")
                continue
        
        # 统计
        roe_count = df['roe'].notna().sum()
        vol_count = df['volatility_20d'].notna().sum()
        rsi_count = df['rsi_14'].notna().sum()
        macd_count = df['macd_histogram'].notna().sum()
        alpha_count = df['alpha001'].notna().sum()
        print(f"✅ 获取到 {roe_count} 只股票的ROE，{vol_count} 只股票的波动率，{rsi_count} 只股票的RSI，{macd_count} 只股票的MACD，{alpha_count} 只股票的Alpha因子")
        
        return df
    
