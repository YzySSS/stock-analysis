        # 计算总分
        score_details['total'] = min(50 + score_details['trend'] + score_details['liquidity'] + 
                                     score_details['valuation'] + score_details['quality'] + 
                                     score_details['volatility'] + score_details.get('rsi', 0) + 
                                     score_details.get('macd', 0) + score_details.get('price_position', 0) +
                                     score_details.get('alpha001', 0) + score_details.get('alpha014', 0) +
                                     score_details.get('alpha028', 0), 100)
        return score_details
    
