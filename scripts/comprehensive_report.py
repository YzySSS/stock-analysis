    def _get_roe_from_sina_web(self, code: str) -> float:
        """从新浪财经网页抓取ROE"""
        try:
            import requests
            from bs4 import BeautifulSoup
            
            url = f"https://finance.sina.com.cn/realstock/company/{code}/finance.shtml"
            headers = {
                'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36'
            }
            response = requests.get(url, headers=headers, timeout=10)
            
            if response.status_code == 200:
                soup = BeautifulSoup(response.text, 'html.parser')
                # 查找ROE数据
                roe_text = soup.find(text=lambda t: '净资产收益率' in str(t))
                if roe_text:
                    import re
                    roe_match = re.search(r'([\d.]+)%', str(roe_text))
                    if roe_match:
                        return float(roe_match.group(1))
        except Exception:
            pass
        return None
    
