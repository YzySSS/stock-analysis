class ComprehensiveStockReport:
    """综合股票报告生成器"""
    
    def __init__(self):
        self.manager = get_data_manager()
        self.positions = get_all_positions()
        self.news_provider = CozeWebSearchProvider()
        self.credibility_checker = NewsCredibilityChecker()
    
    def get_position_analysis(self, code: str, current_price: float) -> dict:
        """获取持仓分析与买卖建议"""
        position = None
        for p in self.positions:
            if p.code == code:
                position = p
                break
        
        if not position:
            return None
        
        profit_pct = ((current_price - position.cost_price) / position.cost_price * 100)
        
        # 买卖建议
        if profit_pct > 15:
            advice = '🔴 强烈建议止盈'
            action = '减仓50%以上，锁定利润'
        elif profit_pct > 10:
            advice = '🟡 建议部分止盈'
            action = '减仓30%，保留底仓观察'
        elif profit_pct > 5:
            advice = '🟢 建议持有'
            action = '趋势向好，继续持有，关注10%止盈点'
        elif profit_pct > -5:
            advice = '⚪ 建议观望'
            action = '震荡整理，设好止损位观察'
        elif profit_pct > -10:
            advice = '🟠 建议关注止损'
            action = '接近止损线，做好减仓准备'
        else:
            advice = '🔴 强烈建议止损'
            action = '已超止损线，严格减仓或清仓'
        
        return {
            'name': position.name,
            'code': code,
            'cost': position.cost_price,
            'current': current_price,
            'profit_pct': profit_pct,
            'advice': advice,
            'action': action
        }
    
    def get_news_with_credibility(self, stock_name: str) -> list:
        """获取新闻并评估可信度"""
        try:
            news_list = self.news_provider.search_stock_news(stock_name, days=3)
            
            results = []
            for news in news_list:
                # 评估可信度
                cred = self.credibility_checker.check_credibility(
                    news.get('url', ''), 
                    news.get('title', '')
                )
                
                level_color = {
                    'S': '🟢', 'A': '🟢', 'B': '🟡', 'C': '🟠', 'D': '🔴'
                }.get(cred.level, '⚪')
                
                results.append({
                    'title': news.get('title', '')[:30] + '...',
                    'url': news.get('url', ''),
                    'level': cred.level,
                    'score': cred.score,
                    'emoji': self.credibility_checker.get_credibility_emoji(cred.level),
                    'reason': cred.reason,
                    'color': level_color
                })
            
            return results
        except Exception as e:
            print(f"新闻获取失败: {e}")
            return []
    
    def _get_strategy_tag(self, row) -> str:
        """获取策略标签"""
        change = row.get('change_percent', 0) or 0
        pe = row.get('pe')
        turnover = row.get('turnover', 0) or 0
        
        if change > 5:
            return "强势突破 - 关注回调买入机会"
        elif change > 2:
            return "趋势向好 - 可逢低关注"
        elif pd.notna(pe) and pe < 20 and change > 0:
            return "价值成长 - 低估+上涨双重逻辑"
        elif turnover > 5e8 and change > 0:
            return "资金关注 - 放量上涨"
        elif change < -3:
            return "回调观察 - 等待企稳信号"
        else:
            return "震荡整理 - 观望为主"
    
    def _try_akshare_screening(self) -> pd.DataFrame:
        """尝试从AkShare获取全市场数据"""
        try:
            if self.manager.ak is None:
                return pd.DataFrame()
            
            df = self.manager.ak.stock_zh_a_spot_em()
            if df is None or df.empty:
                return pd.DataFrame()
            
            # 标准化列名
            df = df.rename(columns={
                '代码': 'code',
                '名称': 'name',
                '最新价': 'price',
                '涨跌幅': 'change_percent',
                '市盈率-动态': 'pe',
                '成交额': 'turnover',
                '换手率': 'turnover_rate',
                '总市值': 'market_cap',
                '所属行业': 'industry'
            })
            
            print(f"✅ AkShare 选股数据获取成功: {len(df)} 只")
            return df
            
        except Exception as e:
            print(f"⚠️ AkShare 选股失败: {e}")
            return pd.DataFrame()
    
