def send_feishu_webhook(content: str, webhook_url: str = None):
    """发送报告到飞书 webhook"""
    import json
    import urllib.request
    import urllib.error
    
    # 默认 webhook URL
    if webhook_url is None:
        webhook_url = "https://open.feishu.cn/open-apis/bot/v2/hook/f70974f2-da1a-47d7-8f48-2bc855dad067"
    
    # 飞书 webhook 格式
    payload = {
        "msg_type": "text",
        "content": {
            "text": content
        }
    }
    
    try:
        headers = {
            'Content-Type': 'application/json'
        }
        data = json.dumps(payload, ensure_ascii=False).encode('utf-8')
        
        req = urllib.request.Request(webhook_url, data=data, headers=headers, method='POST')
        
        with urllib.request.urlopen(req, timeout=30) as response:
            result = json.loads(response.read().decode('utf-8'))
            if result.get('code') == 0:
                print("✅ 报告已推送到飞书 webhook")
                return True
            else:
                print(f"⚠️ 飞书 webhook 返回错误: {result}")
                return False
    except Exception as e:
        print(f"❌ 推送到飞书失败: {e}")
        return False


