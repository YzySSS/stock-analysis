#!/usr/bin/env python3
"""
盘后报告DeepSeek分析提示词模板
用于分析盘前选股策略表现并生成改进建议
"""

PROMPT_TEMPLATE = '''
## 角色设定
你是一位资深的量化交易策略分析师，拥有10年以上A股量化策略开发经验，精通多因子选股模型、风险控制和策略优化。

## 任务
请分析以下股票盘前选股策略的表现数据，并给出专业的诊断报告和改进建议。

---

## 一、策略详情

### 策略名称
版本B (V10+P1+P2+舆情) - 7因子增强选股策略

### 因子配置 (总分100分)

| 因子 | 权重 | 计算方法 |
|------|------|---------|
| 技术 | 18分 | 价格形态、趋势强度、突破信号 |
| 情绪 | 5分 | 技术情绪(简化)，基于涨跌幅度 |
| 板块轮动 | 33分 ⭐ | RPS相对强度、板块动量、龙头识别 |
| 资金流向 | 19分 | 成交量、换手率、资金净流入 |
| 风险 | 15分 | 波动率、60日价格分位、回撤控制 |
| 一致预期 | 5分 | 业绩预期、机构评级 |
| 舆情 | 5分 | 新闻情绪分析(正面/负面/中性) |

### 核心机制

1. **市场强弱综合指数** (0-100分)
   - 趋势得分(40%): 基于大盘指数均线排列
   - 宽度得分(30%): 上涨股票占比
   - 成交量得分(20%): 量能变化
   - 情绪得分(10%): 涨跌比

2. **动态阈值机制**
   - 基础阈值: 60分
   - 动态调整: threshold = 60 + (60 - market_strength) × 0.2
   - 强势市场(>60): 阈值降至55-60分
   - 弱势市场(<40): 阈值提升至70-75分

3. **动态因子权重**
   - 强势市场: 进攻性因子×1.05, 风险因子×0.90
   - 弱势市场: 进攻性因子×0.95, 风险因子×1.20

4. **仓位管理**
   - position_ratio = min(100%, max(20%, market_strength / 50))

---

## 二、历史选股表现数据

{historical_data}

---

## 三、关键问题观察

1. **追涨问题**: 所有推荐股票在推荐时已有一定涨幅(0-5%)
2. **准确率偏低**: 当前准确率36.4%，远低于预期
3. **因子得分同质化**: 多只股票评分集中在同一分数，缺乏区分度
4. **3月26日全错**: 选的都是白酒/保险蓝筹股，但当天都下跌

---

## 四、需要你回答的问题

### 1. 问题诊断
- 为什么3月26日会全错？是市场环境判断失误还是因子失效？
- 为什么所有股票的因子得分都集中在60-71分区间，缺乏区分度？
- 为什么存在追涨问题（推荐时已涨0-5%）？动态阈值没有发挥作用吗？
- 板块轮动因子权重33%是否合理？是否存在过度追逐热点的问题？

### 2. 策略缺陷分析
- 当前7因子配置是否存在冗余或缺失？
- 权重分配是否合理？
- 动态阈值机制的实际效果如何？

### 3. 改进建议
请提供具体的、可落地的改进方案：
- 因子权重如何调整？
- 阈值机制如何优化？
- 是否需要增加新的过滤条件？
- 如何有效避免追涨？

### 4. 风险控制
- 如何防范单日-9%以上的大亏？
- 是否需要增加个股层面的止损机制？
- 仓位管理建议是否需要调整？

---

## 输出格式要求

请以结构化格式输出：

1. **执行摘要** (3-5条核心结论)
2. **详细诊断** (分点论述)
3. **量化分析** (如有计算)
4. **改进方案** (分短期/中期/长期)
5. **具体参数建议** (如权重数值、阈值等)
6. **风险提示**

请用中文回答，语言专业但易懂，避免过于学术化的表达。
'''

def generate_prompt(historical_data_text: str) -> str:
    """生成完整的DeepSeek分析提示词"""
    return PROMPT_TEMPLATE.format(historical_data=historical_data_text)

if __name__ == "__main__":
    # 测试用例
    test_data = """
### 📅 2026-03-23
选股5只，准确率40%，平均收益-1.46%

### 📅 2026-03-26  
选股3只，准确率0%，平均收益-1.72% (全错!)

### 📅 2026-03-27
选股3只，准确率66.7%，平均收益+1.29%

### 整体统计
总选股11只，正确4次，准确率36.4%，平均收益-0.78%
"""
    print(generate_prompt(test_data))
